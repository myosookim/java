package array;

public class EnhancedForLoop {

	public static void main(String[] args) {

		String[] strArray = { "Java" , "Android" , "C" , "JavaScript" , "Python " };
		
		for(String lang : strArray)
		{
			System.out.println(lang);
		}
		
		/*  for(변수 : 배열)
		  	{
		  		반복 실행문;
		 	}
		*/
		
		// 변수에는 배열의 각 요소가 대립함.
	}

}
